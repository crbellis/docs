# Example of operations on variables
x = 5
y = 3
result = x + y
print(result) # print the sum of x + y
result = x * y # reassign result to the product of x * y
print(result)

# Example of shorthand operations
x = 5
x += 3 # equivalent to x = x + 3
print(x)
x -= 3 # equivalent to x = x - 3
print(x)