# Example of variable assignment
message = "Hello, Python!"
count = 42
pi_value = 3.14

message = -12 # message is now an int

# Example of variable re-assignment
message = "Hello, Python!"

print(message) # prints the variable message to the console
print("This is a string literal") # prints the string literal "This is a string literal" to the console
print(5) # prints the integer 5 to the console
print(pi_value) # prints the variable pi_value to the console

sum_result = 5 + 3
difference_result = 10 - 4
